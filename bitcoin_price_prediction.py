import pandas as pd
import seaborn as sns
from scipy.stats import pearsonr
import matplotlib.pyplot as plt

df=pd.read_csv('bitcoin_dataset.csv')
df['btc_market_price'][1023]
df.head()

sns.jointplot(x=df['btc_market_cap'], y=df['btc_market_price'], data = df)

X = df[['btc_market_cap','btc_n_transactions','btc_miners_revenue','btc_cost_per_transaction','btc_hash_rate', 'btc_difficulty','btc_cost_per_transaction_percent']]
Y=df['btc_market_price']

X = X.fillna(X.mean())

from sklearn.linear_model import LinearRegression
from sklearn.model_selection import train_test_split
from sklearn.metrics import mean_squared_error

x_train,x_test,y_train,y_test = train_test_split(X,Y)
clf = LinearRegression()

clf.fit(x_train,y_train)
y_pred = clf.predict(x_test)

mean_squared_error(y_test,y_pred)

